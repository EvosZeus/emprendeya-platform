                                                <div class="card-body">
                                                    <ul>
                                                        <li><strong>Contraseñas Seguras:</strong> Requerimos contraseñas robustas y almacenamos tus contraseñas de forma segura (hashed).</li>
                                                        <li><strong>Autenticación de Múltiples Factores (Próximamente):</strong> Estamos trabajando para implementar la autenticación de múltiples factores (2FA/MFA) como una capa adicional de seguridad para el inicio de sesión.</li>
                                                        <li><strong>Acceso Interno Restringido:</strong> El acceso a tus datos por parte de nuestro personal está limitado al principio de "menor privilegio necesario", lo que significa que solo tienen acceso a la información indispensable para realizar sus funciones.</li>
                                                    </ul>
                                                </div>
                                            </div>
                                        </div>
