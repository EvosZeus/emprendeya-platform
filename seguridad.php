                                    <h4 class="mb-3">Implementando Medidas Concretas para Proteger tus Datos</h4>
                                    <p>La protección de tu información es un proceso integral que abarca diferentes aspectos tecnológicos y operativos. Estas son algunas de las medidas clave que implementamos:</p>
                                    <div class="accordion accordion-secondary">
                                        <div class="card">
                                            <div class="card-header" id="headingSeguridadCifrado" data-bs-toggle="collapse"
                                                data-bs-target="#collapseSeguridadCifrado" aria-expanded="true"
                                                aria-controls="collapseSeguridadCifrado" role="button">
                                                <div class="span-title">
                                                    Cifrado de Datos (Encriptación)
                                                </div>
                                                <div class="span-mode"></div>
                                            </div>
                                            <div id="collapseSeguridadCifrado" class="collapse show" aria-labelledby="headingSeguridadCifrado"
                                                data-parent="#v-pills-proteccion-datos .accordion">
                                                <div class="card-body">
                                                    <ul>
                                                        <li><strong>Datos en Tránsito:</strong> Utilizamos el protocolo HTTPS (Hypertext Transfer Protocol Secure) con cifrado SSL/TLS para proteger la información que intercambias con nuestra plataforma mientras navegas y realizas acciones. Esto significa que la comunicación entre tu navegador y nuestros servidores está encriptada.</li>
                                                        <li><strong>Datos en Reposo:</strong> La información sensible almacenada en nuestras bases de datos, como contraseñas, se guarda utilizando técnicas de hashing y/o cifrado robustas para prevenir su acceso no autorizado incluso en el improbable caso de una brecha.</li>
                                                    </ul>
                                                </div>
                                            </div>
                                        </div>
                                        <div class="card">
                                            <div class="card-header collapsed" id="headingSeguridadInfra" data-bs-toggle="collapse"
                                                data-bs-target="#collapseSeguridadInfra" aria-expanded="false"
                                                aria-controls="collapseSeguridadInfra" role="button">
                                                <div class="span-title">
                                                    Infraestructura Segura
                                                </div>
                                                <div class="span-mode"></div>
                                            </div>
                                            <div id="collapseSeguridadInfra" class="collapse" aria-labelledby="headingSeguridadInfra"
                                                data-parent="#v-pills-proteccion-datos .accordion">
                                                <div class="card-body">
                                                    <ul>
                                                        <li><strong>Servidores Protegidos:</strong> Nuestra plataforma se aloja en centros de datos que cumplen con altos estándares de seguridad física y lógica.</li>
                                                        <li><strong>Firewalls y Sistemas de Detección:</strong> Implementamos firewalls para filtrar el tráfico no deseado y sistemas de detección/prevención de intrusiones (IDS/IPS) para monitorear y bloquear actividades maliciosas.</li>
                                                        <li><strong>Actualizaciones y Parches:</strong> Mantenemos nuestro software y sistemas operativos actualizados con los últimos parches de seguridad para mitigar vulnerabilidades conocidas.</li>
                                                    </ul>
                                                </div>
                                            </div>
                                        </div>
                                        <div class="card">
                                            <div class="card-header collapsed" id="headingSeguridadAcceso" data-bs-toggle="collapse"
                                                data-bs-target="#collapseSeguridadAcceso" aria-expanded="false"
                                                aria-controls="collapseSeguridadAcceso" role="button">
                                                <div class="span-title">
                                                    Control de Acceso y Autenticación
                                                </div>
                                                <div class="span-mode"></div>
